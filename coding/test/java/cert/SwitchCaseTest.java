package test.java.cert;

public class SwitchCaseTest {

	public static void main(String[] args) {
		int x = 400;
		switch (x) {
		case 10:
			System.out.println(10);
		case 20:
			System.out.println(20);
		case 30:
			System.out.println(30);
			break;
		case 40:
			System.out.println(40);
		default:
			System.out.println("default");
		}

	}

}
